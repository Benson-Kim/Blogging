                    <h3>The objective of information system  security </h3>
                        <p>The aim of information system security is to optimize theperformance of an organization with respect to the risks 
                            to which it is exposed. Risk is defined as the chance of injury, damage, or loss. Thus, risk has two elements:</p>
                        <ul>
                            <li>chance—an element of uncertainty</li>
                            <li>potential loss or damage</li>
                        </ul>
                        <p>Except forthe possibility of restitution, information system 
                        security actions taken today work toreducefuturerisk losses. Because of the uncertainty about future risk losses, 
                        perfectsecurity, which implies zero losses, would be infinitely expensive. For this reason, 
                        risk managers strive to optimize the allocation of resources by minimizing the total costof information system 
                        security measures taken and the risk losses experienced. Thisoptimization process is commonly referred to 
                        as risk management. Risk management in this sense is a three-part process:</p>
                        <ol>
                            <li>Identification of material risks</li>
                            <li>Selection and implementation of measures to mitigate the risks</li>
                            <li>Tracking and evaluating of risk losses experienced in order to validate the first two parts of the process</li>
                        </ol>
